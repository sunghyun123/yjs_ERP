// ── 금액 포맷 ─────────────────────────────────────────────────────────────────
function fmt(n: number): string {
  return n.toLocaleString('ko-KR', { maximumFractionDigits: 0 })
}
function fmtDiff(n: number): string {
  return (n >= 0 ? '+' : '') + fmt(n)
}

async function main() {
  // 신ERP: 수주 테이블에서 지중no → id 맵 로드
  const { data: 수주목록 } = await supabase
    .from('수주')
    .select('id, 지중no, 수주금액_공급가, 보험료율, 하도전용율')
  type 수주Row = { id: number; 지중no: string; 수주금액_공급가: number | null; 보험료율: number | null; 하도전용율: number | null }
  const 수주Map = new Map<string, number>(
    (수주목록 as 수주Row[]).map((r) => [r.지중no, r.id]),
  )

  // 2026년 범위
  const START = '2026-01-01'
  const END   = '2027-01-01'

  // 신ERP: 공사이력 2026년 성과금액
  const { data: 이력목록 } = await supabase
    .from('공사이력')
    .select('수주_id, 성과금액')
    .gte('작업일자', START)
    .lt('작업일자', END)
  const 성과Map = new Map<number, number>()
  for (const r of (이력목록 ?? []) as { 수주_id: number; 성과금액: number }[]) {
    성과Map.set(r.수주_id, (성과Map.get(r.수주_id) ?? 0) + (r.성과금액 ?? 0))
  }

  // 공사이력 성과 비교
  console.log('\n=== 성과금액 비교 (구ERP vs 신ERP) ===')
  console.log('※ 신ERP: Supabase 공사이력 합계 / 구ERP: 매출손익현황.xlsx 합계')
  console.log('※ 차이 = 신ERP - 구ERP  (음수: 신ERP가 낮음)\n')

  type Row = { 지중no: string; 구: number; 신: number; diff: number }
  const rows: Row[] = []
  let 합계구 = 0, 합계신 = 0

  for (const [지중no, { 성과: 구성과 }] of Object.entries(구ERP)) {
    const 수주id = 수주Map.get(지중no)
    const 신성과 = 수주id != null ? (성과Map.get(수주id) ?? 0) : 0
    const diff = 신성과 - 구성과
    합계구 += 구성과
    합계신 += 신성과
    if (Math.abs(diff) > 100) {
      rows.push({ 지중no, 구: 구성과, 신: 신성과, diff })
    }
  }

  // 신ERP에만 있는 공사 (구ERP 목록에 없는 지중no)
  const 역수주Map = new Map([...수주Map.entries()].map(([no, id]) => [id, no]))
  for (const [수주id, 신성과] of 성과Map) {
    const 지중no = 역수주Map.get(수주id)
    if (지중no && !(지중no in 구ERP) && 신성과 > 100) {
      rows.push({ 지중no: `[신ERP만] ${지중no}`, 구: 0, 신: 신성과, diff: 신성과 })
      합계신 += 신성과
    }
  }

  rows.sort((a, b) => Math.abs(b.diff) - Math.abs(a.diff))

  console.log('지중No'.padEnd(16) + '구ERP'.padStart(18) + '신ERP'.padStart(18) + '차이'.padStart(18))
  console.log('-'.repeat(70))
  for (const { 지중no, 구, 신, diff } of rows) {
    console.log(
      지중no.padEnd(16) +
      fmt(구).padStart(18) +
      fmt(신).padStart(18) +
      fmtDiff(diff).padStart(18),
    )
  }
  console.log('-'.repeat(70))
  console.log(
    '합계'.padEnd(16) +
    fmt(합계구).padStart(18) +
    fmt(합계신).padStart(18) +
    fmtDiff(합계신 - 합계구).padStart(18),
  )

  // 요약
  const 성과차이 = 합계신 - 합계구
  console.log(`\n[요약] 성과금액 총 차이: ${fmtDiff(성과차이)}원`)
  console.log(`       (신ERP가 구ERP보다 ${성과차이 < 0 ? '낮음' : '높음'})`)
}

main().catch(console.error)
